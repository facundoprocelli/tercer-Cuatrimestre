package Modulo;

public class Ropa {
    private String color;
    private String talle;

    public Ropa(String color, String talle) {
        this.color = color;
        this.talle = talle;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getTalle() {
        return talle;
    }

    public void setTalle(String talle) {
        this.talle = talle;
    }
}
